class Javaque8
{
    public static void main(String args[])
    { 
      String s="Green";
      if(s=="Red")
      {
         System.out.println("colour indicates Stop");      
      }
      else if(s=="Yellow")
      {
          System.out.println("colour indicates Get Ready"); 
      }
      else if(s=="Green")
      {  
           System.out.println("colour indicates Go"); 
      }
      else
      {
            System.out.println("colour indicates nothing"); 
      }

    }
}